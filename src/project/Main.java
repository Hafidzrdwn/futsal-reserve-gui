    public static boolean cekOnlyNumbers(String txt){
        String rgx = "^[0-9]+$";
        
        return txt.matches(rgx);
    }
    
    public static boolean cekOnlyWords(String txt) {
        String rgx = "^[A-Za-z]+$";
        return txt.matches(rgx);
    }
    
    public String getBiayaLap(){
        
        String jenisLap = cmbJenisLap.getSelectedItem().toString();
        
        if(cmbJenisLap.getSelectedIndex() != 0) {
            
            if(rbIndoor.isSelected()) {
                
                switch(jenisLap) {
                    case "Reguler":
                        this.hargaLap = "300000";
                        break;
                    case "Matras":
                        this.hargaLap = "250000";
                        break;
                    case "Rumput":
                        this.hargaLap = "200000";
                        break;
                }
                
                
            } else if(rbOutdoor.isSelected()) {
                
                switch(jenisLap) {
                    case "Reguler":
                        this.hargaLap = "250000";
                        break;
                    case "Matras":
                        this.hargaLap = "200000";
                        break;
                    case "Rumput":
                        this.hargaLap = "150000";
                        break;
                }
            }
            
        } else {
            this.hargaLap = "-";
        }
        
        return this.hargaLap;
    }
    
    public void setHargaLap() {
        String hargaLapTemp = "- / JAM";
        String biaya = (!this.getBiayaLap().equals("-")) ? formatRp(this.getBiayaLap()) : this.getBiayaLap();
        labelHargaLap.setText(biaya + hargaLapTemp.substring(1));
    }
    
    public void setBiayaLap() {
       int lamaSewaTemp = (!this.lamaSewa.isEmpty() && cekOnlyNumbers(this.lamaSewa)) 
               ? Integer.parseInt(this.lamaSewa)
               : 0;
       int hargaLapTemp = (!this.hargaLap.isEmpty() && cekOnlyNumbers(this.hargaLap))? Integer.parseInt(this.hargaLap) : 0;
       this.biayaLap = lamaSewaTemp * hargaLapTemp;
       
       txtBiayaLap.setText(formatRp(String.valueOf(this.biayaLap)));
    }
    
    public void setHargaTambahanSewa() {
        int lamaSewaTemp = (!this.lamaSewa.isEmpty() && cekOnlyNumbers(this.lamaSewa)) 
               ? Integer.parseInt(this.lamaSewa)
               : 0;
        int sepatu = (cbSewaSepatu.isSelected()) ? 50000 : 0,
            kostum = (cbSewaKostum.isSelected()) ? 45000 : 0;
        this.hargaTambahanSewa = lamaSewaTemp * (sepatu + kostum);
        
        txtHargaTambahanSewa.setText(formatRp(String.valueOf(this.hargaTambahanSewa)));
    }
    
    public void setTotalHarga() {
        this.totalHarga = this.biayaLap + this.hargaTambahanSewa;
        txtTotalHarga.setText(formatRp(String.valueOf(this.totalHarga)));
    }
    
    public void resetTotalHarga(){
        this.totalHarga = 0;
        txtTotalHarga.setText("0");
        txtUangKembalian.setText("-");
    }
    
    public String formatRp(String r) {
        Double a;
        a = Double.parseDouble(r);
        
        NumberFormat nf = NumberFormat.getNumberInstance(new Locale("in", "ID"));
        return nf.format(a);
    }
    

