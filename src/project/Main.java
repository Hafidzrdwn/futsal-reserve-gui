    private void btnHitungActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHitungActionPerformed
       if(!this.lamaSewa.isEmpty() && cekOnlyNumbers(this.lamaSewa)) {
           this.setTotalHarga();
           if(!txtUangBayar.getText().isEmpty()) {
               
            int uangBayar = (!txtUangBayar.getText().isEmpty()) ? Integer.parseInt(txtUangBayar.getText().trim()) : 0;
            if(uangBayar == 0) {
                txtUangKembalian.setText("-");
                return;
            }
            
            int kembalianTemp = uangBayar - this.totalHarga;
            
            if(kembalianTemp < 0) {
                txtUangKembalian.setText("Maaf uang anda tidak cukup!");
            } else if(kembalianTemp == 0) {
                txtUangKembalian.setText("-");
            } else {
                this.kembalian = kembalianTemp;
                txtUangKembalian.setText(formatRp(String.valueOf(kembalian)));
            }
               
           }
           
       } else {
           JOptionPane.showMessageDialog(this, "Mohon masukkan lama sewa terlebih dahulu!!", "ERROR", JOptionPane.ERROR_MESSAGE);
       }
    }//GEN-LAST:event_btnHitungActionPerformed

    private void txtUangBayarKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtUangBayarKeyReleased
        if(txtTotalHarga.getText().equals("0")) {
            
            JOptionPane.showMessageDialog(this, "Hitung total harga terlebih dahulu!!", "ERROR", JOptionPane.ERROR_MESSAGE);
            txtUangBayar.setText("");
            btnCetakStruk.setEnabled(false);
            
        } else {
            
           if(!txtUangBayar.getText().isEmpty() && !cekOnlyNumbers(txtUangBayar.getText().trim())) {
               
               JOptionPane.showMessageDialog(this, "Uang bayar harus berupa angka!!", "ERROR", JOptionPane.ERROR_MESSAGE);
               txtUangBayar.setText("");
               btnCetakStruk.setEnabled(false);
               return;
           }
            
            int uangBayar = (!txtUangBayar.getText().isEmpty()) ? Integer.parseInt(txtUangBayar.getText().trim()) : 0;
            if(uangBayar == 0) {
                txtUangKembalian.setText("-");
                btnCetakStruk.setEnabled(false);
                return;
            }
            
            btnCetakStruk.setEnabled(true);
            
            int kembaliantemp = uangBayar - this.totalHarga;
            
            if(kembaliantemp < 0) {
                txtUangKembalian.setText("Maaf uang anda tidak cukup!");
            } else if(kembaliantemp == 0) {
                txtUangKembalian.setText("-");
            } else {
                this.kembalian = kembaliantemp;
                txtUangKembalian.setText(formatRp(String.valueOf(kembalian)));
            }
            
        }
    }//GEN-LAST:event_txtUangBayarKeyReleased

    private void btnCetakStrukActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCetakStrukActionPerformed
        if(txtUangKembalian.getText().equals("Maaf uang anda tidak cukup!")) {
            JOptionPane.showMessageDialog(this, "Pembayaran Gagal! Uang anda tidak cukup!", "ERROR", JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        if(txtTotalHarga.getText().equals("0")) {
            JOptionPane.showMessageDialog(this, "Hitung total harga terlebih dahulu!", "ERROR", JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        if(txtNamaPenyewa.getText().trim().isEmpty() && txtNoTelp.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Lengkapi data anda dengan benar!", "ERROR", JOptionPane.ERROR_MESSAGE);
            return;
        } else {
            
            if(!cekOnlyWords(txtNamaPenyewa.getText().trim())) {
                JOptionPane.showMessageDialog(this, "Nama penyewa tidak boleh mengandung angka!", "ERROR", JOptionPane.ERROR_MESSAGE);
                return;
            }
            
            if(!cekOnlyNumbers(txtNoTelp.getText().trim())) {
                JOptionPane.showMessageDialog(this, "No Telp harus berupa angka!", "ERROR", JOptionPane.ERROR_MESSAGE);
                return;
            }
        }
        
        Struk struk = new Struk();
        
        
        String nama = txtNamaPenyewa.getText().trim();
        String notelp = txtNoTelp.getText().trim();
        String durasi = txtOrderDurasi.getText().trim();
        
        String tipe = "";
        if(rbIndoor.isSelected()) tipe = "INDOOR";
        if(rbOutdoor.isSelected()) tipe = "OUTDOOR";
        if(!rbIndoor.isSelected() && !rbOutdoor.isSelected()) tipe = "-";
        
        String jenis = (cmbJenisLap.getSelectedIndex() != 0) ?  cmbJenisLap.getSelectedItem().toString().toUpperCase() : "-";
        String hargaLapTemp = (this.biayaLap == 0) ? "-" : String.valueOf(this.biayaLap);
        
        struk.setData(nama,notelp,durasi,tipe,jenis, cbSewaSepatu.isSelected(), cbSewaKostum.isSelected(),
                hargaLapTemp, String.valueOf(this.hargaTambahanSewa), String.valueOf(this.totalHarga),
                txtUangBayar.getText().trim(), String.valueOf(this.kembalian));
        
        struk.setVisible(true);
        dispose();
    }//GEN-LAST:event_btnCetakStrukActionPerformed

